package entuty;

import java.io.Serializable;

public class FictionInfo implements Serializable{
	private static final long serialVersionUID = 8526642605305523894L;
	private String title;  //小说标题
	private String author; //小说作者
	private String date;   //小说上传日期
	private String path;   //小说路径
	private String dict;   //小说描述
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getDict() {
		return dict;
	}
	public void setDict(String dict) {
		this.dict = dict;
	}	
}
